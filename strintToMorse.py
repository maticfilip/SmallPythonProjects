def main():
    text=input("Input the text you would like to translate to morse:").upper()
    split_text=list(text)
    print(split_text)
    translation=[]
    for char in split_text:
        translation+= (MORSE_CODE_DICT[char])
    print(translation)

if __name__=="__main__":
    main()
